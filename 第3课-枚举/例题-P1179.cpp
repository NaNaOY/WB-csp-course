/**
 * 例题1：数字统计（洛谷 P1179）
 * 
 * 题目：统计区间 [L, R] 的所有整数中，数字 2 出现的次数。
 * 
 * 难度：红题 ⭐
 * 算法：简单枚举 + 数字各位分离
 * 时间复杂度：O((R-L) * log10(R))
 * 
 * 测试样例：
 *   输入：2 22
 *   输出：6
 *   解释：2(1个), 12(1个), 20(1个), 21(1个), 22(2个) → 共6个
 */

#include <bits/stdc++.h>
using namespace std;

// 统计数字 n 中数字 2 出现的次数
int count2(int n) {
    int cnt = 0;
    while (n > 0) {
        if (n % 10 == 2) cnt++;  // 个位是2？
        n /= 10;                  // 去掉个位
    }
    return cnt;
}

int main() {
    int L, R;
    cin >> L >> R;
    
    int ans = 0;
    for (int i = L; i <= R; i++) {  // 枚举 [L, R] 中每个数
        ans += count2(i);            // 累加该数中 2 的个数
    }
    
    cout << ans << endl;
    return 0;
}

/*
 * 解题思路：
 *   1. 枚举区间 [L, R] 中的每一个整数
 *   2. 对每个整数，逐位分离数字（%10 取个位，/10 去掉个位）
 *   3. 统计个位等于 2 的次数，累加到答案
 * 
 * 复杂度分析：
 *   - 循环次数：(R-L+1)，最多 100,000
 *   - 每次 count2：log10(R) 次操作，最多约 6 次
 *   - 总操作：约 6×10^5，完全在时限内（C++ 1秒 ≈ 10^8）
 * 
 * 易错点：
 *   - 数字 0 时 while(n>0) 不会执行，但本题 L>=1 所以无影响
 */
