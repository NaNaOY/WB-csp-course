/**
 * 例题2：火柴棒等式（洛谷 P1149）
 * 
 * 题目：用 n 根火柴棒拼出形如 A+B=C 的等式（A,B,C为非负整数）。
 *       数字0~9分别需要6,2,5,5,4,5,6,3,7,6根火柴。
 *       「+」和「=」各需要2根。
 * 
 * 难度：橙题 ⭐⭐⭐
 * 算法：预处理 + 枚举
 * 时间复杂度：O(1000 * 1000) = O(10^6)
 * 
 * 测试样例：
 *   输入：18
 *   输出：9
 */

#include <bits/stdc++.h>
using namespace std;

// 数字 0~9 各需要的火柴棒数
const int COST[10] = {6, 2, 5, 5, 4, 5, 6, 3, 7, 6};

// 预处理：计算 0~999 每个数需要多少火柴棒（查表 O(1)）
vector<int> f(1000);

void build() {
    for (int i = 0; i <= 999; i++) {
        if (i == 0) {
            f[i] = COST[0];           // 数字 0 需要 6 根
        } else {
            int sum = 0, x = i;
            while (x > 0) {
                sum += COST[x % 10];  // 逐位累加
                x /= 10;
            }
            f[i] = sum;
        }
    }
}

int main() {
    int n;
    cin >> n;
    
    build();        // 预处理火柴消耗表
    
    int ans = 0;
    // 枚举 A 和 B（C 通过 A+B 计算得出，不需要枚举）
    for (int a = 0; a <= 999; a++) {
        for (int b = 0; b <= 999; b++) {
            int c = a + b;
            if (c > 999) continue;      // C 超过范围，跳过
            
            // n-4: 减去「+」和「=」各 2 根
            if (f[a] + f[b] + f[c] + 4 == n) {
                ans++;
            }
        }
    }
    
    cout << ans << endl;
    return 0;
}

/*
 * 解题思路：
 *   1. 预处理 0~999 每个数字的火柴消耗（查表法，O(1)）
 *   2. 枚举 A 和 B（0~999），C = A+B 自动得出
 *   3. 判断 f[A] + f[B] + f[C] + 4 == n？是则计数
 * 
 * 为什么枚举到 999？
 *   n <= 24，减去「+」「=」各2根，剩20根给 A+B+C
 *   数字1最省火柴（2根），最多组成约 10 位
 *   枚举到 999（3位，最多 3×7=21 根）足够
 * 
 * 复杂度分析：
 *   - 预处理：O(1000 * log10(1000)) ≈ 4000 次操作
 *   - 枚举：1000 * 1000 = 10^6 次循环
 *   - 每次查表 O(1)，总操作约 10^6，稳稳过
 * 
 * 关键技巧：
 *   - 预处理/查表法：把重复计算的结果提前存好
 *   - 减范围：C 不用枚举，直接算出
 *   - 边界特判：0 需要单独处理（while(n>0) 会跳过）
 */
