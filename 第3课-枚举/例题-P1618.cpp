/**
 * 例题3：三连击（升级版）（洛谷 P1618）
 * 
 * 题目：将 1~9 这9个数字分成三组，分别组成三个三位数，
 *       且这三个三位数的比例是 A:B:C。
 *       输出所有满足条件的三个三位数，无解输出 No!!!。
 * 
 * 难度：橙题 ⭐⭐⭐
 * 算法：全排列枚举（next_permutation）
 * 时间复杂度：O(9!) = O(362,880)
 * 
 * 测试样例：
 *   输入：1 2 3
 *   输出：
 *     192 384 576
 *     219 438 657
 *     273 546 819
 *     327 654 981
 */

#include <bits/stdc++.h>
using namespace std;

int main() {
    int A, B, C;
    cin >> A >> B >> C;
    
    // 初始排列：1, 2, ..., 9
    vector<int> p = {1, 2, 3, 4, 5, 6, 7, 8, 9};
    bool found = false;
    
    do {
        // 前3个数字 → 第一个三位数
        int x = p[0] * 100 + p[1] * 10 + p[2];
        // 中间3个数字 → 第二个三位数
        int y = p[3] * 100 + p[4] * 10 + p[5];
        // 最后3个数字 → 第三个三位数
        int z = p[6] * 100 + p[7] * 10 + p[8];
        
        // 验证比例关系：x : y : z = A : B : C
        // 用乘法避免浮点数精度问题
        // x/A == y/B  ⇔  x * B == y * A
        if (x * B == y * A && x * C == z * A) {
            cout << x << " " << y << " " << z << endl;
            found = true;
        }
    } while (next_permutation(p.begin(), p.end()));
    
    if (!found) {
        cout << "No!!!" << endl;
    }
    
    return 0;
}

/*
 * 解题思路：
 *   1. 用 next_permutation 遍历 1~9 的所有排列（共 9! = 362,880 种）
 *   2. 将每个排列分为三组（前3个、中3个、后3个），组成三个三位数
 *   3. 用乘法验证比例关系
 * 
 * 为什么用乘法？
 *   浮点数除法 1.0*x/A == 1.0*y/B 可能因为精度问题判错
 *   整数乘法 x*B == y*A 精确无误差！
 * 
 * 复杂度分析：
 *   - 排列总数：9! = 362,880
 *   - 每次常量时间，总操作约 3.6×10^5
 *   - 完全在时限内（C++ 1秒 ≈ 10^8）
 * 
 * next_permutation 用法：
 *   - 头文件：<algorithm>
 *   - 原型：bool next_permutation(first, last)
 *   - 将 [first, last) 重排为下一个字典序更大的排列
 *   - 如果已经是最大排列（降序），返回 false 并重置为最小排列
 *   - 使用前提：初始序列必须是最小排列（升序）
 */
