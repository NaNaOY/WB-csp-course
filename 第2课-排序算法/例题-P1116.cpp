/**
 * 例题：车厢重组
 * 来源：洛谷 P1116
 * 难度：⭐（入门）
 * 
 * 题目：一列火车有 N 节车厢，编号 1~N 但不按顺序。
 *       每次只能交换相邻两节车厢，问最少需要多少次交换
 *       才能让车厢按 1~N 的顺序排列？
 * 
 * 解题思路：
 *   冒泡排序的交换次数恰好等于逆序对数量。
 *   直接模拟冒泡排序，统计交换次数即可。
 *   N ≤ 100，O(N²) 完全可行。
 * 
 * 进阶：用归并排序求逆序对，可做到 O(N log N)，处理 N ≤ 10^5。
 * 
 * 测试样例：
 *   输入：
 *     4
 *     4 3 2 1
 *   输出：
 *     6
 *   解释：
 *     初始：4 3 2 1
 *     第1轮：3 4 2 1 → 3 2 4 1 → 3 2 1 4（3次交换）
 *     第2轮：2 3 1 4 → 2 1 3 4（2次交换）
 *     第3轮：1 2 3 4（1次交换）
 *     总共 3+2+1 = 6
 */

#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cin >> n;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }

    int swaps = 0;

    // 冒泡排序，同时统计交换次数
    for (int i = 0; i < n - 1; i++) {
        for (int j = 0; j < n - 1 - i; j++) {
            if (a[j] > a[j + 1]) {
                swap(a[j], a[j + 1]);
                swaps++;  // 发生交换，计数+1
            }
        }
    }

    cout << swaps << endl;
    return 0;
}
