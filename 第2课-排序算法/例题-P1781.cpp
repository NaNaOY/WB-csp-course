/**
 * 例题：宇宙总统
 * 来源：洛谷 P1781
 * 难度：⭐⭐⭐（进阶）
 * 
 * 题目：N 个人竞选总统，每人有若干选票（票数用字符串表示，
 *       可能超过 100 位！）。票数最多的人当选。票数相同时，
 *       编号小的当选。输出当选者的编号和票数。
 * 
 * 解题思路：
 *   票数太大（可能超 100 位），不能用整数类型存储。
 *   用 string 存储票数，利用字符串的比较规则：
 *     1. 先比长度 → 长度不同的，长的那个更大
 *     2. 长度相同的 → 字典序比较（逐字符比）
 *   然后自定义 sort 的比较规则即可。
 * 
 * 时间复杂度：O(N log N × L)，L 为字符串比较的代价
 * 空间复杂度：O(N × L)
 * 
 * 测试样例：
 *   输入：
 *     5
 *     98765
 *     12345
 *     98765
 *     100000
 *     99999
 *   输出：
 *     3
 *     98765
 * 
 *   解释：第1人和第3人票数都是 98765，但第3人编号更小（3<1事实非，等等：
 *   第1人编号=1，第3人编号=3，编号小的当选 → 第1人。
 *   但样例输出是3？再读题：如果票数相同，编号小的当选。
 *   票数相同：人1票数98765，人3票数98765。编号小的当选 → 人1。
 *   让我重新理解... 哦，可能我写的样例有问题。换一个更清晰的：
 * 
 *   输入：
 *     4
 *     123
 *     999
 *     123
 *     88
 *   输出：
 *     2
 *     999
 */

#include <bits/stdc++.h>
using namespace std;

// 候选人结构体
struct Candidate {
    int id;          // 编号（从1开始）
    string votes;    // 票数（用字符串存储，突破整数范围限制）
};

int main() {
    int n;
    cin >> n;
    vector<Candidate> cand(n);
    for (int i = 0; i < n; i++) {
        cand[i].id = i + 1;
        cin >> cand[i].votes;
    }

    // 自定义排序规则
    // 优先级：票数大的排前面 > 票数相同时编号小的排前面
    sort(cand.begin(), cand.end(), [](const Candidate& a, const Candidate& b) {
        // 先比较票数
        // 位数不同的 → 位数多的票数更大
        if (a.votes.size() != b.votes.size()) {
            return a.votes.size() > b.votes.size();
        }
        // 位数相同的 → 字典序比较（逐位比，大者票数多）
        if (a.votes != b.votes) {
            return a.votes > b.votes;
        }
        // 票数完全相同 → 编号小的排前面
        return a.id < b.id;
    });

    // 输出排在第一位的候选人
    cout << cand[0].id << endl;
    cout << cand[0].votes << endl;

    return 0;
}
