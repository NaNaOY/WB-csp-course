/**
 * 例题：P1045 麦森数
 * 来源：洛谷 P1045（NOIP 2003 普及组）
 * 难度：普及+/提高（黄题）
 * 
 * 题目描述：
 *   形如 2^P - 1 的素数称为麦森数（Mersenne Prime）。
 *   给定 P（P ≤ 3100000），计算 2^P - 1 的位数和最后 500 位数字。
 *   输出：
 *     第一行：位数（十进制）
 *     接下来 10 行，每行 50 个数字：最后 500 位（不足补前导零）
 * 
 * 解题思路：
 *   核心挑战：2^3100000 约有 933190 位，绝不可能完整存储！
 *   
 *   技巧1：快速幂（二进制分解幂运算）
 *     2^13 = 2^(1101)₂ = 2^8 × 2^4 × 2^1
 *     每次将 base 平方，遇到二进制位为 1 就乘到结果里
 *   
 *   技巧2：只保留后 500 位
 *     模运算性质：(a × b) % MOD = ((a % MOD) × (b % MOD)) % MOD
 *     这里不需要显式取模，只需在乘法时限制结果长度 ≤ 500
 *   
 *   技巧3：位数公式
 *     digits(2^P) = floor(P × log10(2)) + 1
 *     2^P - 1 和 2^P 位数相同（当 P > 0 时）
 *   
 *   技巧4：快速幂 + 压位乘法
 *     乘法只保留后500位，复杂度 O(log P × 500²)
 *     对于 P ≤ 3100000，log₂(3100000) ≈ 22 次乘法
 *     每次乘法 500×500 = 250000 次循环，总共约 550 万次操作
 *     C++ 可以在 1 秒内完成
 * 
 * 时间复杂度：O(log P × 500²)
 * 空间复杂度：O(500)
 */

#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

const int KEEP = 500;  // 只保留后 500 位

/**
 * 高精度乘法（只保留后 KEEP 位）
 * 思路：标准高精度乘法，但限制 i+j < KEEP 且结果长度 ≤ KEEP
 */
vector<int> mul500(vector<int>& A, vector<int>& B) {
    int n = (int)A.size();
    int m = (int)B.size();
    int len = min(n + m, KEEP);           // 结果最多 500 位
    vector<int> C(len, 0);
    
    // 逐位相乘累加（只处理贡献到前 500 位的部分）
    for (int i = 0; i < n && i < KEEP; i++)
        for (int j = 0; j < m && i + j < KEEP; j++)
            C[i + j] += A[i] * B[j];
    
    // 统一处理进位（500位之内）
    int t = 0;
    for (int i = 0; i < len; i++) {
        t += C[i];
        C[i] = t % 10;
        t /= 10;
    }
    // 超出 500 位的进位直接丢弃
    
    // 去除前导零（保证 C 的长度）
    while (C.size() > 1 && C.back() == 0)
        C.pop_back();
    return C;
}

/**
 * 快速幂：计算 2^exp 的后 KEEP 位
 * 
 * 基本原理（以 2^13 为例）：
 *   13 = (1101)₂ = 8 + 4 + 1
 *   2^13 = 2^8 × 2^4 × 2^1
 * 
 * 算法流程：
 *   res = 1, base = 2
 *   遍历 exp 的二进制位：
 *     - 如果当前位是 1：res = res × base
 *     - base = base × base（底数自乘）
 *     - exp 右移一位
 */
vector<int> qpow(int exp) {
    vector<int> res = {1};           // res = 2^0 = 1
    vector<int> base = {2};          // base = 2^1 = 2
    
    while (exp > 0) {
        if (exp & 1) {               // 当前二进制位为 1
            res = mul500(res, base); // res = res * base
        }
        base = mul500(base, base);   // base = base^2
        exp >>= 1;                   // exp = exp / 2
    }
    return res;
}

int main() {
    int P;
    cin >> P;
    
    // 1. 计算位数：floor(P * log10(2)) + 1
    int digits = (int)(P * log10(2)) + 1;
    cout << digits << endl;
    
    // 2. 快速幂计算 2^P 的后 500 位
    vector<int> ans = qpow(P);
    
    // 3. 处理 2^P - 1：个位减 1
    //    由于 2^P 最后一位不可能是 0，所以直接减 1 不会导致借位问题
    //    但如果结果是 [..., 0] 则需要借位处理
    ans[0] -= 1;
    for (int i = 0; ans[i] < 0 && i < ans.size(); i++) {
        ans[i] += 10;                // 不够减，借 1 当 10
        if (i + 1 < ans.size())
            ans[i + 1] -= 1;         // 高位减 1
    }
    
    // 4. 输出：共 500 位，每行 50 个数字，共 10 行
    //    不足 500 位的前面补 0
    for (int i = KEEP - 1; i >= 0; i--) {
        if (i < (int)ans.size())
            cout << ans[i];
        else
            cout << 0;               // 前导零补位
        if ((KEEP - i) % 50 == 0)    // 每 50 个换行
            cout << endl;
    }
    
    return 0;
}

